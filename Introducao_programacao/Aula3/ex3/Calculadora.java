package Aula3.ex3;

public class Calculadora {

    double num1, num2, total;
    double somar(){
        total =  num1 + num2;
        return total;
    }
    double subtrair(){
        total =  num1 - num2;
        return total;
    }
    double dividir(){
        total =  num1 / num2;
        return total;
    }
    double multiplicar(){
        total =  num1 * num2;
        return total;
    }
}
